<?php
$x = 7;
$y = 3;
echo "OPERASI ARITMATIKA DASAR <br>";
echo "\$x = $x <br>";
echo "\$y = $y <br>";
echo "<br>";

// Penjumlahan
echo '$x + $y = ';
print $x + $y;
echo "<br>";

// Pengurangan
echo '$x - $y = ';
print $x - $y;
echo "<br>";

// Perkalian
echo '$x * $y = ';
print $x * $y;
echo "<br>";

// Pembagian
echo '$x / $y = ';
print $x / $y;
echo "<br>";

// Modulus (Sisa Pembagian)
echo '$x % $y = ';
print $x % $y;
echo "<br>";

// Eksponen (Pangkat)
echo '$x ** $y = '; // 7 dipangkatkan 3
print $x ** $y;
echo "<br>";
?>